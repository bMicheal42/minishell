#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	cc;
	char	*ss;

	cc = (char)c;
	ss = (char *)s;
	while (*ss)
		ss++;
	while (ss != s)
	{
		if (*ss == cc)
			return (ss);
		ss--;
	}
	if (*ss == cc)
		return (ss);
	return (0);
}
