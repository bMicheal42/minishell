#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*ret;
	size_t	i;

	ret = (char *)malloc(ft_strlen(s) + 1);
	if (!ret)
		return (0);
	i = 0;
	while (s[i])
	{
		ret[i] = s[i];
		i++;
	}
	ret[i] = 0;
	return (ret);
}
