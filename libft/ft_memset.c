#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*dst;
	unsigned char	uc;

	uc = (unsigned char)c;
	dst = (unsigned char *)b;
	while (len--)
		*dst++ = uc;
	return (b);
}
