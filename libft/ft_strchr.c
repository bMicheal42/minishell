#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	uc;
	char	*ss;

	uc = (char)c;
	ss = (char *)s;
	while (*ss)
	{
		if (*ss == uc)
			return (ss);
		ss++;
	}
	if (*ss == uc)
		return (ss);
	return (0);
}
