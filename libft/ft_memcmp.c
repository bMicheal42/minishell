#include "libft.h"

int	ft_memcmp(const void *a, const void *b, size_t n)
{
	unsigned char	*ua;
	unsigned char	*ub;

	ua = (unsigned char *)a;
	ub = (unsigned char *)b;
	while (n--)
	{
		if (*ua != *ub)
			return (*ua - *ub);
		ua++;
		ub++;
	}
	return (0);
}
